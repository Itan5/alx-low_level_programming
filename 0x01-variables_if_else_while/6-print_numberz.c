#include <stdio.h>
/**
 * main - Entry point of program
 *
 *Description: Prints "all single digit numbers of base 10 starting from 0"
 *
 * Return: Always 0 (success)
 */
int main(void)
{
	int sinint;

	sinint = 0;

	while
		(sinint <= 9)

	{
		putchar('0' + sinint);
		sinint++;
	}
	{

		putchar('\n');
	}
	return (0);
}

